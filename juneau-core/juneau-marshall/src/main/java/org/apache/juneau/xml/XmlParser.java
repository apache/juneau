/**
 * Parses text generated by the {@link XmlSerializer} class back into a POJO model.
 *
 * <h5 class='topic'>Media types</h5>
 * <p>
 * Handles <c>Content-Type</c> types:  <bc>text/xml</bc>
 *
 * <h5 class='topic'>Description</h5>
 * <p>
 * See the {@link XmlSerializer} class for a description of Juneau-generated XML.
 *
 * <h5 class='figure'>Example input (Map of name/age):</h5>
 * <p class='bxml'>
 * 	<xt>&lt;object&gt;</xt>
 * 		<xt>&lt;name&gt;</xt>Alice<xt>&lt;/name&gt;</xt>
 * 		<xt>&lt;age&gt;</xt>30<xt>&lt;/age&gt;</xt>
 * 	<xt>&lt;/object&gt;</xt>
 * </p>
 *
 * <h5 class='figure'>Complex (nested object + array):</h5>
 * <p class='bxml'>
 * 	<xt>&lt;object&gt;</xt>
 * 		<xt>&lt;name&gt;</xt>Alice<xt>&lt;/name&gt;</xt>
 * 		<xt>&lt;age&gt;</xt>30<xt>&lt;/age&gt;</xt>
 * 		<xt>&lt;address&gt;</xt>
 * 			<xt>&lt;street&gt;</xt>123 Main St<xt>&lt;/street&gt;</xt>
 * 			<xt>&lt;city&gt;</xt>Boston<xt>&lt;/city&gt;</xt>
 * 			<xt>&lt;state&gt;</xt>MA<xt>&lt;/state&gt;</xt>
 * 		<xt>&lt;/address&gt;</xt>
 * 		<xt>&lt;tags&gt;</xt>
 * 			<xt>&lt;string&gt;</xt>a<xt>&lt;/string&gt;</xt>
 * 			<xt>&lt;string&gt;</xt>b<xt>&lt;/string&gt;</xt>
 * 			<xt>&lt;string&gt;</xt>c<xt>&lt;/string&gt;</xt>
 * 		<xt>&lt;/tags&gt;</xt>
 * 	<xt>&lt;/object&gt;</xt>
 * </p>
 *
 * <h5 class='section'>Notes:</h5><ul>
 * 	<li class='note'>This class is thread safe and reusable.
 * </ul>
 *
 * <h5 class='section'>See Also:</h5><ul>
 * 	<li class='link'><a class="doclink" href="https://juneau.apache.org/docs/topics/XmlBasics">XML Basics</a>

 * </ul>
 */
@SuppressWarnings({
	"java:S115" // Constants use UPPER_snakeCase convention
})
public class XmlParser extends ReaderParser implements XmlMetaProvider {

	// Argument name constants for assertArgNotNull
	private static final String ARG_copyFrom = "copyFrom";

	// Property name constants
	private static final String PROP_eventAllocator = "eventAllocator";
	private static final String PROP_preserveRootElement = "preserveRootElement";
	private static final String PROP_reporter = "reporter";
	private static final String PROP_resolver = "resolver";
	private static final String PROP_validating = "validating";

	/**
	 * Builder class.
	 */
	public abstract static class Builder<SELF extends Builder<SELF>> extends ReaderParser.Builder<SELF> {

		private static final Cache<HashKey,XmlParser> CACHE = Cache.of(HashKey.class, XmlParser.class).build();

		private boolean preserveRootElement;
		private boolean validating;
		private Class<? extends XMLEventAllocator> eventAllocator;
		private Class<? extends XMLReporter> reporter;
		private Class<? extends XMLResolver> resolver;

		/**
		 * Constructor, default settings.
		 */
		protected Builder() {
			consumes("text/xml,application/xml");
			preserveRootElement = env("XmlParser.preserveRootElement", false);
			validating = env("XmlParser.validating", false);
			eventAllocator = null;
			reporter = null;
			resolver = null;
		}

		/**
		 * Copy constructor.
		 *
		 * @param copyFrom The builder to copy from.
		 * 	<br>Cannot be <jk>null</jk>.
		 */
		protected Builder(Builder<?> copyFrom) {
			super(assertArgNotNull(ARG_copyFrom, copyFrom));
			preserveRootElement = copyFrom.preserveRootElement;
			validating = copyFrom.validating;
			eventAllocator = copyFrom.eventAllocator;
			reporter = copyFrom.reporter;
			resolver = copyFrom.resolver;
		}

		/**
		 * Copy constructor.
		 *
		 * @param copyFrom The bean to copy from.
		 * 	<br>Cannot be <jk>null</jk>.
		 */
		protected Builder(XmlParser copyFrom) {
			super(assertArgNotNull(ARG_copyFrom, copyFrom));
			preserveRootElement = copyFrom.preserveRootElement;
			validating = copyFrom.validating;
			eventAllocator = copyFrom.eventAllocator;
			reporter = copyFrom.reporter;
			resolver = copyFrom.resolver;
		}

		@Override /* Overridden from Context.Builder<?> */
		public XmlParser build() {
			return cache(CACHE).build(XmlParser.class);
		}

		@Override /* Overridden from Context.Builder<?> */
		public abstract SELF copy();

		/**
		 * XML event allocator.
		 *
		 * <p>
		 * Associates an {@link XMLEventAllocator} with this parser.
		 *
		 * @param value The new value for this property.
		 * 	<br>Can be <jk>null</jk> (no event allocator will be used).
		 * @return This object.
		 */
		public SELF eventAllocator(Class<? extends XMLEventAllocator> value) {
			eventAllocator = value;
			return self();
		}

		@Override /* Overridden from Context.Builder<?> */
		public HashKey hashKey() {
			// @formatter:off
			return HashKey.of(
				super.hashKey(),
				preserveRootElement,
				validating,
				eventAllocator,
				reporter,
				resolver
			);
			// @formatter:on
		}

		/**
		 * Preserve root element during generalized parsing.
		 *
		 * <p>
		 * When enabled, when parsing into a generic {@link JsonMap}, the map will contain a single entry whose key
		 * is the root element name.
		 *
		 * <h5 class='section'>Example:</h5>
		 * <p class='bjava'>
		 * 	<jc>// Parser with preserve-root-element.</jc>
		 * 	ReaderParser <jv>parser1</jv> = XmlParser
		 * 		.<jsm>create</jsm>()
		 * 		.preserveRootElement()
		 * 		.build();
		 *
		 * 	<jc>// Parser without preserve-root-element (the default behavior).</jc>
		 * 	ReaderParser <jv>parser2</jv> = XmlParser
		 * 		.<jsm>create</jsm>()
		 * 		.build();
		 *
		 * 	String <jv>xml</jv> = <js>"&lt;root&gt;&lt;a&gt;foobar&lt;/a&gt;&lt;/root&gt;"</js>;
		 *
		 * 	<jc>// Produces:  "{ root: { a:'foobar' }}"</jc>
		 * 	JsonMap <jv>map1</jv> = <jv>parser1</jv>.parse(<jv>xml</jv>, JsonMap.<jk>class</jk>);
		 *
		 * 	<jc>// Produces:  "{ a:'foobar' }"</jc>
		 * 	JsonMap <jv>map2</jv> = <jv>parser2</jv>.parse(<jv>xml</jv>, JsonMap.<jk>class</jk>);
		 * </p>
		 *
		 * @return This object.
		 */
		public SELF preserveRootElement() {
			return preserveRootElement(true);
		}

		/**
		 * Same as {@link #preserveRootElement()} but allows you to explicitly specify the value.
		 *
		 * @param value The value for this setting.
		 * @return This object.
		 */
		public SELF preserveRootElement(boolean value) {
			preserveRootElement = value;
			return self();
		}

		/**
		 * XML reporter.
		 *
		 * <p>
		 * Associates an {@link XMLReporter} with this parser.
		 *
		 * @param value The new value for this property.
		 * 	<br>Can be <jk>null</jk> (no reporter will be used).
		 * @return This object.
		 */
		public SELF reporter(Class<? extends XMLReporter> value) {
			reporter = value;
			return self();
		}

		/**
		 * XML resolver.
		 *
		 * <p>
		 * Associates an {@link XMLResolver} with this parser.
		 *
		 * @param value The new value for this property.
		 * 	<br>Can be <jk>null</jk> (no resolver will be used).
		 * @return This object.
		 */
		public SELF resolver(Class<? extends XMLResolver> value) {
			resolver = value;
			return self();
		}


		/**
		 * Enable validation.
		 *
		 * <p>
		 * If <jk>true</jk>, XML document will be validated.
		 *
		 * <p>
		 * See {@link XMLInputFactory#IS_VALIDATING} for more info.
		 *
		 * @return This object.
		 */
		public SELF validating() {
			return validating(true);
		}

		/**
		 * Same as {@link #validating()} but allows you to explicitly specify the value.
		 *
		 * @param value The value for this setting.
		 * @return This object.
		 */
		public SELF validating(boolean value) {
			validating = value;
			return self();
		}
	}

	/**
	 * Concrete default builder leaf for the non-subclassed {@link XmlParser#create()} / {@link XmlParser#copy()} path.
	 */
	public static final class DefaultBuilder extends Builder<DefaultBuilder> {

		DefaultBuilder() {}

		DefaultBuilder(XmlParser copyFrom) {
			super(copyFrom);
		}

		DefaultBuilder(Builder<?> copyFrom) {
			super(copyFrom);
		}

		@Override /* Overridden from Context.Builder<?> */
		public DefaultBuilder copy() {
			return new DefaultBuilder(this);
		}
	}

	/** Default parser, all default settings.*/
	public static final XmlParser DEFAULT = new XmlParser(create());

	/**
	 * Creates a new builder for this object.
	 *
	 * @return A new builder.
	 */
	public static Builder<?> create() {
		return new DefaultBuilder();
	}

	protected final boolean preserveRootElement;
	protected final boolean validating;
	protected final Class<? extends XMLEventAllocator> eventAllocator;
	protected final Class<? extends XMLReporter> reporter;
	protected final Class<? extends XMLResolver> resolver;

	private final XMLEventAllocator eventAllocatorImpl;
	private final XMLReporter reporterImpl;
	private final XMLResolver resolverImpl;
	private final Map<BeanMeta<?>,XmlBeanMeta> xmlBeanMetas = new ConcurrentHashMap<>();
	private final Map<BeanPropertyMeta,XmlBeanPropertyMeta> xmlBeanPropertyMetas = new ConcurrentHashMap<>();
	private final Map<ClassMeta<?>,XmlClassMeta> xmlClassMetas = new ConcurrentHashMap<>();

	/**
	 * Constructor.
	 *
	 * @param builder
	 * 	The property store containing all the settings for this object.
	 */
	public XmlParser(Builder<?> builder) {
		super(builder);
		eventAllocator = builder.eventAllocator;
		preserveRootElement = builder.preserveRootElement;
		reporter = builder.reporter;
		resolver = builder.resolver;
		validating = builder.validating;

		reporterImpl = nn(reporter) ? newInstance(reporter) : null;
		resolverImpl = nn(resolver) ? newInstance(resolver) : null;
		eventAllocatorImpl = nn(eventAllocator) ? newInstance(eventAllocator) : null;
	}

	@Override /* Overridden from Context */
	public Builder<?> copy() {
		return new DefaultBuilder(this);
	}

	@Override /* Overridden from Context */
	public XmlParserSession.Builder<?> createSession() {
		return XmlParserSession.create(this);
	}

	@Override /* Overridden from Context */
	public XmlParserSession getSession() { return createSession().build(); }

	@Override /* Overridden from XmlMetaProvider */
	public XmlBeanMeta getXmlBeanMeta(BeanMeta<?> bm) {
		return xmlBeanMetas.computeIfAbsent(bm, k -> new XmlBeanMeta(k, this));
	}

	@Override /* Overridden from XmlMetaProvider */
	public XmlBeanPropertyMeta getXmlBeanPropertyMeta(BeanPropertyMeta bpm) {
		return xmlBeanPropertyMetas.computeIfAbsent(bpm, k -> new XmlBeanPropertyMeta(k.getDelegateFor(), this));
	}

	@Override /* Overridden from XmlMetaProvider */
	public XmlClassMeta getXmlClassMeta(ClassMeta<?> cm) {
		return xmlClassMetas.computeIfAbsent(cm, k -> new XmlClassMeta(k, this));
	}

	private static <T> T newInstance(Class<T> c) {
		try {
			return c.getDeclaredConstructor().newInstance();
		} catch (Exception e) {
			throw toRex(e);
		}
	}

	/**
	 * XML event allocator.
	 *
	 * @see Builder#eventAllocator(Class)
	 * @return
	 * 	The {@link XMLEventAllocator} associated with this parser, or <jk>null</jk> if there isn't one.
	 */
	protected final XMLEventAllocator getEventAllocator() { return eventAllocatorImpl; }

	/**
	 * XML reporter.
	 *
	 * @see Builder#reporter(Class)
	 * @return
	 * 	The {@link XMLReporter} associated with this parser, or <jk>null</jk> if there isn't one.
	 */
	protected final XMLReporter getReporter() { return reporterImpl; }

	/**
	 * XML resolver.
	 *
	 * @see Builder#resolver(Class)
	 * @return
	 * 	The {@link XMLResolver} associated with this parser, or <jk>null</jk> if there isn't one.
	 */
	protected final XMLResolver getResolver() { return resolverImpl; }

	/**
	 * Preserve root element during generalized parsing.
	 *
	 * @see Builder#preserveRootElement()
	 * @return
	 * 	<jk>true</jk> if when parsing into a generic {@link JsonMap}, the map will contain a single entry whose key
	 * 	is the root element name.
	 */
	protected final boolean isPreserveRootElement() { return preserveRootElement; }

	/**
	 * Enable validation.
	 *
	 * @see Builder#validating()
	 * @return
	 * 	<jk>true</jk> if XML document will be validated.
	 */
	protected final boolean isValidating() { return validating; }

	@Override /* Overridden from ReaderParser */
	protected FluentMap<String,Object> properties() {
		return super.properties()
			.a(PROP_eventAllocator, eventAllocator)
			.a(PROP_preserveRootElement, preserveRootElement)
			.a(PROP_reporter, reporter)
			.a(PROP_resolver, resolver)
			.a(PROP_validating, validating);
	}
}